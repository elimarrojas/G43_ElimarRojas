    private void tbOwnersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbOwnersMouseClicked
        // TODO add your handling code here:
        int row = tbOwners.getSelectedRow();
        System.out.println(row);
        if(row < 0){
            JOptionPane.showMessageDialog(this, "Debes seleccionar un dueño de mascota");
        }else{
            int id  = Integer.parseInt((String)tbOwners.getValueAt(row, 0).toString());
            String owner  = tbOwners.getValueAt(row, 1).toString();
            String document_type = (String)tbOwners.getValueAt(row, 2).toString();
            int document  = Integer.parseInt((String)tbOwners.getValueAt(row, 3).toString());
            String contact  = tbOwners.getValueAt(row, 4).toString();
            String gender = (String)tbOwners.getValueAt(row, 5);
            System.out.println(id + " - " + owner + " - " + document_type+ " - " + document + " - " + contact+ " - " + gender);            
            txtIdOwner.setText("" + id);
            txtOwner.setText(owner);
            cbDocType.setSelectedItem(document_type);
            txtNumDoc.setText("" + document);
            txtContact.setText(contact);
            if(gender.equals("Femenino"))
                rbFem.setSelected(true);
            if(gender.equals("Masculino"))
                rbMan.setSelected(true);
        }

    }//GEN-LAST:event_tbOwnersMouseClicked

