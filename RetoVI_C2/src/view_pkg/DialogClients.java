    void showOwners() {
        String sql = "SELECT * FROM `tb_owner` WHERE 1";
        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            //Los datos que devuelve la consulta se muestran en la tabla
            Object[] owner = new Object[6];
            modelo = (DefaultTableModel) tbOwners.getModel();
            while (rs.next()) {
                owner[0] = rs.getInt("id");
                owner[1] = rs.getString("owner");
                owner[2] = rs.getString("document_type");
                owner[3] = rs.getInt("document");
                owner[4] = rs.getInt("contact");
                owner[5] = rs.getString("gender");
                modelo.addRow(owner);
                System.out.println(rs.getInt("id"));
            }
            tbOwners.setModel(modelo);
        } catch (SQLException e) {

        }
    }
    
    void addOwner() {        
        String owner = txtOwner.getText();
        String document_type = cbDocType.getSelectedItem().toString();
        int document = Integer.parseInt(txtNumDoc.getText());
        String contact = txtContact.getText();
        String gender = "";
        if (rbFem.isSelected()) {
            gender = "Femenino";
        } else {
            gender = "Masculino";
        }

        if (owner.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del dueño de la mascota");
        } else {
            String query = "INSERT INTO `tb_owner`(`owner`, `document_type`, `document`, `contact`, `gender`) VALUES('" + owner + "', '" + document_type + "'," + document + ",'" + contact + "', '" + gender + "')";

            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El cliente ha sido creado");
                clearTable();
                showOwners();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "No se pudo crear el cliente");
            }
        }
    }

    void editOwner() {        
        int id = Integer.parseInt(txtIdOwner.getText());
        String owner = txtOwner.getText();
        String document_type = cbDocType.getSelectedItem().toString();
        int document = Integer.parseInt(txtNumDoc.getText());
        String contact = txtContact.getText();
        String gender;
        if (rbFem.isSelected()) {
            gender = "Femenino";
        } else {
            gender = "Masculino";
        }
        if (owner.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del dueño");
        } else {
            String query = "UPDATE tb_owner SET owner = '" + owner + "', document_type= '" + document_type + "', document = " + document + ", contact= '" + contact + "',gender= '" + gender + "' WHERE id = " + id;            
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El cliente ha sido modificado con éxito");
                clearTable();
                showOwners();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "No se pudo modificar la información del dueño");
            }
        }
    }

    void deleteOwner() {
        int fila = tbOwners.getSelectedRow();
        int id = Integer.parseInt(txtIdOwner.getText());
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "No has seleccionado un dueño de mascota");
        } else {

            System.out.println("ID: " + id);
            String query = "DELETE FROM tb_owner WHERE id = " + id;
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El dueño ha sido eliminado con exito");
                clearTable();
                showOwners();
            } catch (HeadlessException | SQLException e) {
            }
        }
    }

    void clearTable() {
        for (int i = 0; i < tbOwners.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
        txtIdOwner.setText("");
        txtOwner.setText("");
        txtNumDoc.setText("");
        txtContact.setText("");
    }   
    

